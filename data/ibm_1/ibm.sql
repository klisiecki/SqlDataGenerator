SELECT
    RV_ENFCOMPLAINTS.ENF_CSE_TYP_ID,
    RV_ENFCOMPLAINTS.ENF_REGN_ID,
    RV_ENFCOMPLAINTS.BOARDCODE,
    RV_ENFCOMPLAINTS.LICCODE,
    RV_ENFCOMPLAINTS.ACTIVITYDATE,
    RV_ENFCOMPLAINTS.PENDINGENDDATE,
    RV_ENFCOMPLAINTS.ACTV_STRT_DTE,
    RV_ENFCOMPLAINTS.ACTV_END_DTE,
    RV_ENFCOMPLAINTS.CATEGORYORDER,
    RV_ENFCOMPLAINTS.ROD_REOPENDATE,
    RV_ENFCOMPLAINTS.ROD_CLOSEDATE,
    RV_ENFCOMPLAINTS.CLOSUREROWNUMBER
FROM
    RV_ENFCOMPLAINTS_DCA RV_ENFCOMPLAINTS
WHERE

(
  (
     (RV_ENFCOMPLAINTS.CATEGORYORDER in (4,6,8,10,12,13,15,16,18,19,21,22,24,25,26,27,28,29,40,41,42,43) )
      AND (     RV_ENFCOMPLAINTS.ACTIVITYDATE >= '2014-02-01 00:00:00'
            AND RV_ENFCOMPLAINTS.ACTIVITYDATE  < '2014-02-15 00:00:00' )
      AND (RV_ENFCOMPLAINTS.CLOSUREROWNUMBER=0  OR RV_ENFCOMPLAINTS.CLOSUREROWNUMBER=1)
  )
 OR
  (
      RV_ENFCOMPLAINTS.CATEGORYORDER=23
      AND  RV_ENFCOMPLAINTS.ACTV_STRT_DTE<  '2014-02-15 00:00:00'
      AND (RV_ENFCOMPLAINTS.PENDINGENDDATE IS NULL
            OR RV_ENFCOMPLAINTS.ACTV_END_DTE   >= '2014-02-14 00:00:00'
          )
  )
 OR
  (
      RV_ENFCOMPLAINTS.CATEGORYORDER in (14, 17,20, 44)
      AND ( RV_ENFCOMPLAINTS.PENDINGENDDATE IS NULL OR RV_ENFCOMPLAINTS.PENDINGENDDATE >=  '2014-02-01 00:00:00' )
  )
);

