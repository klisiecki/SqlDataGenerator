DROP TABLE PRODUCTS;
DROP TABLE CLIENTS;
DROP TABLE ORDERS;

CREATE TABLE PRODUCTS (
	ID NUMBER(4),
	PRICE NUMBER(6,2),
	CATEGORY VARCHAR2(50),
	DESCRIPTION VARCHAR2(50),
	NAME VARCHAR2(50)
);

CREATE TABLE CLIENTS (
	ID NUMBER(4),
	FIRST_NAME VARCHAR2(50),
	LAST_NAME VARCHAR2(50)
);

CREATE TABLE ORDERS (
	ID NUMBER(6),
	PRODUCT_ID NUMBER(4),
	CLIENT_ID NUMBER(4)
);
