DROP TABLE IF EXISTS PRODUCTS;
DROP TABLE IF EXISTS CLIENTS;
DROP TABLE IF EXISTS ORDERS;

CREATE TABLE PRODUCTS (
  ID INT,
  NAME VARCHAR,
  DESCRIPTION VARCHAR,
  PRICE FLOAT,
  OLD_PRICE FLOAT,
  CATEGORY VARCHAR,
  PACKAGE_WIDTH INT,
  PACKAGE_HEIGHT INT,
  PACKAGE_DEPTH INT
);

CREATE TABLE CLIENTS (
  ID INT,
  FIRST_NAME VARCHAR,
  LAST_NAME VARCHAR,
  BIRTH_DATE TIMESTAMP
);

CREATE TABLE ORDERS (
  ID INT,
  PRODUCT_ID INT,
  CLIENT_ID INT,
  DATETIME TIMESTAMP,
  STATE INT
);