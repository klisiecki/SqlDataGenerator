DROP TABLE IF EXISTS PRODUCTS;
DROP TABLE IF EXISTS CLIENTS;
DROP TABLE IF EXISTS ORDERS;

CREATE TABLE PRODUCTS (
  ID INT,
  NAME VARCHAR,
  DESCRIPTION VARCHAR,
  PRICE FLOAT,
  CATEGORY VARCHAR
);

CREATE TABLE CLIENTS (
  ID INT,
  FIRST_NAME VARCHAR,
  LAST_NAME VARCHAR
);

CREATE TABLE ORDERS (
  ID INT,
  PRODUCT_ID INT,
  CLIENT_ID INT,
  DATETIME TIMESTAMP
);